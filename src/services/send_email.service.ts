import nodemailer from 'nodemailer';

interface ISendEmail {
  to: string;
  user_name: string;
  content: string;
  attachment?: string;
  subject?: string;
  cc?: string;
}

class SendEmailService {
  async execute({
    to,
    user_name,
    content,
    attachment,
    subject,
    cc,
  }: ISendEmail) {
    try {
      let transporter = nodemailer.createTransport({
        service: 'gmail',
        auth: {
          user: 'suporte2@slpart.com.br',
          pass: process.env.EMAIL_KEY,
        },
      });

      let thereIsAttachment: string;
      if (attachment) {
        thereIsAttachment = attachment.split('/')[1];
      }

      const configMail = {
        from: {
          name: 'Suport Bot',
          address: 'suporte2@slpart.com.br',
        },
        to,
        cc,
        subject: ` ${
          subject
            ? `${subject}: Novo chamado recebido!`
            : 'Seu chamado foi recebido com sucesso'
        } `,
        html: email_template(content, user_name, thereIsAttachment),
        attachments: attachment
          ? [
              {
                filename: thereIsAttachment,
                path: attachment,
                cid: thereIsAttachment,
              },
            ]
          : null,
      };

      await transporter.sendMail(configMail);
    } catch (error) {
      console.error(error);
    }
  }
}

